package com.cgvsu.math.vector;

public class Vector3{


    public float x, y, z;

    public Vector3(float x, float y, float z ) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float getX() {
        return this.x;
    }

    public float getY() {
        return this.y;
    }

    public  float getZ() {
        return z;
    }

}
