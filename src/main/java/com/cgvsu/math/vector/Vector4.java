package com.cgvsu.math.vector;

public class Vector4 {

    private float x, y, z, k;

    public Vector4(float x, float y, float z, float k) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.k = k;
    }

    public float getX() {
        return this.x;
    }

    public float getY() {
        return this.y;
    }

    public  float getZ() {
        return z;
    }

    public float getK() {
        return k;
    }
}
